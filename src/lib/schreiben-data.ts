const SCHREIBEN_DATA: Record<string, SchreibAufgabe[]> = {
  a1: A1_AUFGABEN,
  a2: A2_AUFGABEN,
  b1: B1_AUFGABEN,
  b2: B2_AUFGABEN,
  c1: C1_AUFGABEN,
};

export function getSchreibAufgaben(level: string): SchreibAufgabe[] {
  return SCHREIBEN_DATA[level.toLowerCase()] ?? [];
}

export function getSchreibAufgabe(level: string, id: number): SchreibAufgabe | undefined {
  return getSchreibAufgaben(level).find((a) => a.id === id);
}

export type SchreibenProgress = Record<number, { score: number; completedAt: string }>;

export function loadSchreibenProgress(level: string): SchreibenProgress {
  if (typeof window === "undefined") return {};
  try {
    const raw = localStorage.getItem(`schreiben_progress_${level.toLowerCase()}`);
    return raw ? JSON.parse(raw) : {};
  } catch {
    return {};
  }
}

export function saveSchreibenProgress(level: string, aufgabeId: number, score: number) {
  if (typeof window === "undefined") return;
  const progress = loadSchreibenProgress(level);
  progress[aufgabeId] = { score, completedAt: new Date().toISOString() };
  localStorage.setItem(`schreiben_progress_${level.toLowerCase()}`, JSON.stringify(progress));
}
